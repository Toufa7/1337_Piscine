#include <unistd.h>
#include <stdio.h>
int	ft_is_prime(int nb)
{
	int	x;

	x = 2;
	if (nb == 2147483647)
		return (1);
	if (nb <= 1)
		return (0);
	while (x++ <= nb / 2)
	{
		if (nb % x == 0)
			return (0);
	}
	return (1);
}
